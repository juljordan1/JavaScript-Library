// assignment Operator
let r = 5;

//Addition Assignment
r += 1; // r = r + 1

//Subtraction Assignment

r -= 1; // r = r - 1

// Multiplication

r *= 1; // r = r * 1

//Division Assignment

r /= 1; // r = r / 1

// Remainder ( or modulus) Assignment
r %= 1 // r = r % 1

// Exponential Assigment

r **=1;  // r = r ** 1